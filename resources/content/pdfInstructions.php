  <div class="section-title">How to Convert PDF Poster to Image for Website</div>
  <ol>
    <li>Go to <a href="http://pdf2png.com/" target="_blank">http://pdf2png.com/</a></li>
    <li>Click "<i class="fa fa-folder-open"></i> UPLOAD FILES" and select your pdf file.
      <ul>
        <li>Alternatively, drag the pdf file to "Drop your Files Here"</li>
      </ul>
    </li>
    <li>After uploading and converting, click "DOWNLOAD"</li>
    <li>Open the .zip folder downloaded</li>
    <li>Click on "Extract all files" in the top left of the Explorer</li>
    <li>Click "Extract"</li>
    <li>Open some sort of image editor, like Paint, Paint.NET, Photoshop, GIMP, etc.</li>
    <li>Open the image that is in the new folder created from extracting.</li>
    <li>Resize the image to be 150px wide and 210px tall.</li>
    <li>Save the image.</li>
    <li>Upload the resized image in the download field above.</li>
  </ol>
