					<div class="section-title">Principal Wendy Mayea, 2015</div>

					March 16, 2015

					<p>
						As we end School of Choice Month, I am excited by the increased energy and activity that spring brings to Santa Susana High School.
					</p>

					<p>
						Excellence in teaching and learning continues as our teachers work hard incorporating the Common Core State Standards, Next Generation Science Standards, and Career Education Standards into their lesson plans. As I walk through our classrooms, I can see how these foundational changes equate to an even more intense level of engagement for our students. Students discuss math word problems using academic vocabulary, play content-building board games, laugh more during class, focus intently on senior projects, and contribute to classroom, electronic, and partnered discussions. Teachers inspire students' collective creativity, critical thinking, communication, and collaboration. This is how teaching and learning should happen.
					</p>

					<p>
						Santa Susana’s application for the <strong><i>California State Gold Ribbon School</i></strong> award was recently accepted. Part of this process includes a validation visit, where we will highlight our classrooms, courses of study, certification process, CTE-AME programs, Common Core implementation, and technology integration.
					</p>

					<p>
						<strong>We prepare kids for college</strong>, most through the curriculum and daily classroom instruction. We also held a College Fair and a New SAT presentation. Both​ were largely attended and revered.
					</p>

					<p>
						At the <i>Advanced Placement Parent Night</i> on Monday, March 16th, ​parents learned about the academic rigor and best practices offered through our current 18-AP classes. Next year, we are adding two more: AP Studio Art Drawing and AP 3-D Design. ​Teachers were be on-hand to offer information for each AP course offered.
					</p>

					<p>
						<strong>Career Education</strong> evolves at Santa Susana.​ Last week, teachers and staff enthusiastically volunteered to conduct interviews and welcomed our equally enthusiastic incoming 2015-2016 students.​ These students chose Santa Susana to be able to focus their education in their interests, often in both accelerated academics and the arts.
					</p>

					<p>
						Next year, <strong><i>Freshman Seminar</i></strong> will replace Computer Applications. Freshman Seminar uses the Get Focused, Stay Focused curriculum to guide students in the development of a 10-year college and career plan. Santa Susana’s staff has written the curriculum in a way that integrates Computer Applications skills while ensuring all students are: 1) Planning through college and beyond; and 2) Receiving three units of college credit for the course’s completion. To open up their schedule for more subject-specific electives, we encourage incoming freshmen to complete this course through Simi Valley Unified School District's Summer School program. More information on Summer School <a href="http://www.simi.k12.ca.us/cms/page_view?d=x&piid=&vpid=1279458937149" target="_blank">can be found here</a>.
					</p>

					<p>
						Through our <strong><i>Distinguished Speaker Series</i></strong>, a professor from the New York Film Academy, ​presented on ​the Origin of the Universe and ​ also hosted three workshops, open to students and community, before to the lecture: Game Design; Acting, &amp; Visual Effects​;and Bridging Post-Secondary Studies, Career, and the Arts​.
					</p>

					<p>
						Finally, we continue to build links between college readiness, career education and the arts. One way that we're doing this is to integrate <strong>World Languages</strong> (Spanish, French, and German) into our Hospitality, Tourism, and Recreation Pathway. Students will attend field trips to culturally relevant sites, learn about entrepreneurship, and receive guest lectures – all in the language of their choosing!
					</p>

					<p>
						For me,
					</p>

					<p>
						<strong>Spring Activity</strong> was best highlighted by​ a recent Thursday evening at Santa Susana. As soon as classes let out, the <i>Zumba Club</i> danced away in the Student Activities Room, while Play Productio​n's technical rehearsal continued until 8 p.m.​ for their upcoming performance ​of Get Smart​. The <i>Cuesta College Jazz Choir</i> performed​ with two​ Santa Susana alumni, who currently attend Cuesta.​ At the same time, two <i>Dance Level II</i> concerts were held in the tiny Black Box Theatre, and were followed by a reception. The concerts provided an incredible opportunity to stretch ​advanced ​dancers’ skills​ as​ the audience​ sat​ ​just inches from the dancers, observing details of expression, movement, and costume. <strong>Senior projects</strong> continue and, most recently, Sara Gilbert​, directed and performed the ​musical, <i>Surely That’s Wrong</i>, to a sold out audience!
					</p>

					<p>
						Out of classroom learning abounds these past two weeks. Children’s Theater performs at a local elementary schools. Key Club attended its annual convention in Anaheim, and Vocal Music performed at the Heritage Festival.​ This comes just after Santa Susana hosted the 5th Annual Instrumental Music Festival. With the plethora of opportunity, students are taught to be focused and highly-disciplined as they also prepare for continued top-level performance on important exams; High School Exit Exam, CAASPP, Advanced Placement, and the SAT/ACT.
					</p>

					<p>
						As collective troubadours, the dedication of staff, students, parents, and community partners to go above and beyond is what makes Santa Susana High School a rich high school experience for our students. We are excited about our educational opportunities and your continued partnership as we continue to lead the way!
					</p>

					<p>Sincerely,</p>

